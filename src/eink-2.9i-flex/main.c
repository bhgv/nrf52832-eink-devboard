static void pic_display_4bit(){
  u32 i,j;
  u8 temp1,temp2,temp3;

  //old  data
  EPD_SendCommand(0x10);

  for(i=0;i<4736;i++)	               //4736*4  296*128
  { 
    temp3=0;
    for(j=0;j<4;j++){
      temp1 = gImage_1[i*4+j];
      temp2 = temp1&0xF0 ;
      if(temp2 == 0xF0)
        temp3 |= 0x01;//white
      else if(temp2 == 0x00)
        temp3 |= 0x00;  //black
      else if((temp2>0xA0)&&(temp2<0xF0))
        temp3 |= 0x01;  //gray1
      else 
        temp3 |= 0x00; //gray2

      temp3 <<= 1;
      temp1 <<= 4;
      temp2 = temp1&0xF0 ;
      if(temp2 == 0xF0)  //white
        temp3 |= 0x01;
      else if(temp2 == 0x00) //black
        temp3 |= 0x00;
      else if((temp2>0xA0)&&(temp2<0xF0))
        temp3 |= 0x01; //gray1
      else
        temp3 |= 0x00;	//gray2

      if(j!=3)
        temp3 <<= 1;
    }
    EPD_SendData(temp3);
  }

  //new  data
  EPD_SendCommand(0x13);

  for(i=0;i<4736;i++)	               //4736*4   296*128
  {
    temp3=0;
    for(j=0;j<4;j++){
      temp1 = gImage_1[i*4+j];
      temp2 = temp1 & 0xF0;
      if(temp2 == 0xF0)
        temp3 |= 0x01;//white
      else if(temp2 == 0x00)
        temp3 |= 0x00;  //black
      else if((temp2 > 0xA0) && (temp2 < 0xF0))
        temp3 |= 0x00;  //gray1
      else
        temp3 |= 0x01; //gray2

      temp3 <<= 1;
      temp1 <<= 4;
      temp2 = temp1 & 0xF0;
      if(temp2 == 0xF0)  //white
        temp3 |= 0x01;
      else if(temp2 == 0x00) //black
        temp3 |= 0x00;
      else if((temp2>0xA0)&&(temp2<0xF0))
        temp3 |= 0x00;//gray1
      else
        temp3 |= 0x01;	//gray2

      if(j!=3)
        temp3 <<= 1;

    }
    EPD_SendData(temp3);			
  }
}













